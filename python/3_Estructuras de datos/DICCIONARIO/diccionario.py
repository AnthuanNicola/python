'''1. ¿Qué es un Diccionario en Python?
Un diccionario es una colección de datos que almacena pares clave:valor.'''


# Ejemplo de un diccionario simple
persona = {
    "nombre": "Ana",
    "edad": 30,
    "ciudad": "Lima"
}

print(persona)
print(persona["edad"])


'''"nombre", "edad" y "ciudad" son claves (keys).

"Ana", 30 y "Lima" son sus valores correspondientes.'''